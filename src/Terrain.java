package src;

/**
 * Enumeration to classify specific colors of dens and special terrain in the <code>Tile</code> class.
 */
public enum Terrain{
    /**
     * Red colored Den Terrain
     */
    DEN_RED,

    /**
     * Blue colored Den Terrain
     */
    DEN_BLUE, 
    /**
     * Trap Terrain
     */
    TRAP, 
    /**
     * River Terrain
     */
    RIVER
}
